/**
 * Returns the result of concatenation of two strings.
 *
 * @param {string} value1
 * @param {string} value2
 * @return {string}
 *
 * @example
 *   'aa', 'bb' => 'aabb'
 *   'aa',''    => 'aa'
 *   '',  'bb'  => 'bb'
 */
function concatenateStrings(recived1, recived2) {
  //super awesome comment for testing purposes
  return (recived1 + recived2).trim();
}

/**
 * Returns the length of given string.
 *
 * @param {string} value
 * @return {number}
 *
 * @example
 *   'aaaaa' => 5
 *   'b'     => 1
 *   ''      => 0
 */
function getStringLength(recivedStr) {
  return recivedStr.length;
}

/**
 * Returns the result of string template and given parameters firstName and lastName.
 * Please do not use concatenation, use template string :
 * https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/template_strings
 *
 * @param {string} firstName
 * @param {string} lastName
 * @return {string}
 *
 * @example
 *   'John','Doe'      => 'Hello, John Doe!'
 *   'Chuck','Norris'  => 'Hello, Chuck Norris!'
 */
function getStringFromTemplate(recivedFirstname, recivedSurename) {
  return `Hello, ${recivedFirstname} ${recivedSurename}!`;
}

/**
 * Extracts a name from template string 'Hello, First_Name Last_Name!'.
 *
 * @param {string} value
 * @return {string}
 *
 * @example
 *   'Hello, John Doe!' => 'John Doe'
 *   'Hello, Chuck Norris!' => 'Chuck Norris'
 */
function extractNameFromTemplate(recivedString) {
  return `${recivedString.split(' ')[1]} ${recivedString
    .split(' ')[2]
    .slice(0, -1)}`;
}

/**
 * Returns a first char of the given string.
 *
 * @param {string} value
 * @return {string}
 *
 * @example
 *   'John Doe'  => 'J'
 *   'cat'       => 'c'
 */
function getFirstChar(recived) {
  return recived[0];
}

/**
 * Removes a leading and trailing whitespace characters from string.
 *
 * @param {string} value
 * @return {string}
 *
 * @example
 *   '  Abracadabra'    => 'Abracadabra'
 *   'cat'              => 'cat'
 *   '\tHello, World! ' => 'Hello, World!'
 */
function removeLeadingAndTrailingWhitespaces(recived) {
  return recived.trim();
}

/**
 * Returns a string that repeated the specified number of times.
 *
 * @param {string} value
 * @param {string} count
 * @return {string}
 *
 * @example
 *   'A', 5  => 'AAAAA'
 *   'cat', 3 => 'catcatcat'
 */
function repeatString(recivedString, count) {
  return recivedString.repeat(count);
}

/**
 * Remove the first occurrence of string inside another string
 *
 * @param {string} str
 * @param {string} value
 * @return {string}
 *
 * @example
 *   'To be or not to be', 'not'  => 'To be or  to be'
 *   'I like legends', 'end' => 'I like legs',
 *   'ABABAB','BA' => 'ABAB'
 */
function removeFirstOccurrences(initialStr, strToRemove) {
  return initialStr.replace(strToRemove, '');
}

/**
 * Remove the first and last angle brackets from tag string
 *
 * @param {string} str
 * @return {string}
 *
 * @example
 *   '<div>' => 'div'
 *   '<span>' => 'span'
 *   '<a>' => 'a'
 */
function unbracketTag(recivedString) {
  const regexp = new RegExp(/([<>])/, 'g');
  return recivedString.replace(regexp, '');
}

/**
 * Converts all characters of the specified string into the upper case
 *
 * @param {string} str
 * @return {string}
 *
 * @example
 *   'Thunderstruck' => 'THUNDERSTRUCK'
 *  'abcdefghijklmnopqrstuvwxyz' => 'ABCDEFGHIJKLMNOPQRSTUVWXYZ'
 */
function convertToUpperCase(recivedString) {
  return recivedString.toUpperCase();
}

/**
 * Extracts e-mails from single string with e-mails list delimeted by semicolons
 *
 * @param {string} str
 * @return {array}
 *
 * @example
 *   'angus.young@gmail.com;brian.johnson@hotmail.com;bon.scott@yahoo.com'
 *   => [
 *      'angus.young@gmail.com',
 *      'brian.johnson@hotmail.com',
 *      'bon.scott@yahoo.com'
 *   ],
 *   'info@gmail.com' => ['info@gmail.com']
 */
function extractEmails(recivedString) {
  return recivedString.split(';');
}

/**
 * Returns the string representation of rectangle with specified width and height
 * using pseudograhic chars
 *
 * @param {number} width
 * @param {number} height
 * @return {string}
 *
 * @example
 *
 *            '┌────┐\n'+
 *  (6,4) =>  '│    │\n'+
 *            '│    │\n'+
 *            '└────┘\n'
 *
 *  (2,2) =>  '┌┐\n'+
 *            '└┘\n'
 *
 *             '┌──────────┐\n'+
 *  (12,3) =>  '│          │\n'+
 *             '└──────────┘\n'
 *
 */
function getRectangleString(width, height) {
  let initContainer = new Array(height).fill(new Array(width).fill(' '));
  // hard copy array to avoid licked data
  initContainer = JSON.parse(JSON.stringify(initContainer));

  initContainer = initContainer.map((curArrAsString, curIndex) => {
    if (curIndex === 0) {
      curArrAsString.fill('─');
      // eslint-disable-next-line no-param-reassign
      curArrAsString[0] = '┌';
      // eslint-disable-next-line no-param-reassign
      curArrAsString[width - 1] = '┐';
    }

    // rectangle body fill
    if (curIndex !== 0 && curIndex !== height - 1) {
      // eslint-disable-next-line no-param-reassign
      curArrAsString[0] = '│';
      // eslint-disable-next-line no-param-reassign
      curArrAsString[width - 1] = '│';
    }

    // last line
    if (curIndex === height - 1) {
      curArrAsString.fill('─');
      // eslint-disable-next-line no-param-reassign
      curArrAsString[0] = '└';
      // eslint-disable-next-line no-param-reassign
      curArrAsString[width - 1] = '┘';
    }
    return curArrAsString;
  });

  return initContainer
    .map((curent) => {
      curent.push('\n');
      return curent.join('');
    })
    .join('');
}
/**
 * Encode specified string with ROT13 cipher
 * See details:  https://en.wikipedia.org/wiki/ROT13
 *
 * @param {string} str
 * @return {string}
 *
 * @example
 *
 *   'hello' => 'uryyb'
 *   'Why did the chicken cross the road?' => 'Jul qvq gur puvpxra pebff gur ebnq?'
 *   'Gb trg gb gur bgure fvqr!' => 'To get to the other side!'
 *   'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz'
 *    => 'NOPQRSTUVWXYZABCDEFGHIJKLMnopqrstuvwxyzabcdefghijklm'
 *
 */
function encodeToRot13(str) {
  const lowercaseLetters = 'abcdefghijklmnopqrstuvwxyz';

  const splitedStr = str.split('');
  // array with inital alphabet
  const encDict = [...lowercaseLetters];

  // encoding

  const getArrShiftPosition = (recivedChar) => {
    const isUppercase = recivedChar.toUpperCase() === recivedChar;
    // eslint-disable-next-line no-param-reassign
    recivedChar = recivedChar.toLowerCase();
    if (encDict.indexOf(recivedChar) === -1) {
      return recivedChar;
      // throw new Error(`Cant find character ${recivedChar}`);
    }

    const initalCharPos = encDict.indexOf(recivedChar);
    const n = encDict.length;
    const shiftedCharPos = encDict[(((initalCharPos + 13) % n) + n) % n];

    return isUppercase ? shiftedCharPos.toUpperCase() : shiftedCharPos;
  };

  const result = splitedStr.map((x) => getArrShiftPosition(x));

  return result.join('');
}

/**
 * Returns true if the value is string; otherwise false.
 * @param {string} value
 * @return {boolean}
 *
 * @example
 *   isString() => false
 *   isString(null) => false
 *   isString([]) => false
 *   isString({}) => false
 *   isString('test') => true
 *   isString(new String('test')) => true
 */
function isString(recivedString) {
  if (typeof recivedString === 'string' || recivedString instanceof String) {
    return true;
  }
  return false;
}

